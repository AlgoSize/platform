#!/usr/bin/env node
// One-shot migration tool: KV → D1 (Task #25).
//
// Reads the existing user records and run history out of Cloudflare KV
// (USERS namespace + RUNS namespace) and emits SQL that, when applied via
// `wrangler d1 execute`, populates the new `users` and `runs` tables in
// the D1 database.
//
// USAGE
//   # 1. Get the namespace IDs for the OLD USERS + RUNS KV namespaces.
//   #    The Worker's wrangler.toml no longer has a RUNS binding (Task #25
//   #    removed it), so we can't reach RUNS via --binding any more — pass
//   #    --runs-namespace-id directly. List ids with:
//   wrangler kv namespace list
//
//   # 2. Generate the SQL bundle, apply to D1, and emit a diff report:
//   node worker/scripts/migrate-kv-to-d1.mjs \
//        --env production \
//        --users-namespace-id <USERS-id> \
//        --runs-namespace-id <RUNS-id> \
//        --apply algosize
//
//   # The --users-namespace-id flag is optional: if omitted, the script
//   # falls back to `--binding USERS` (which still exists in wrangler.toml
//   # for the quota counters). --runs-namespace-id is REQUIRED unless you
//   # temporarily re-add a RUNS binding to wrangler.toml or pass --skip-runs.
//   #
//   # --apply runs `wrangler d1 execute <db> --file=… --remote` for you,
//   # then queries `SELECT COUNT(*)` from users + runs and prints a
//   # source-vs-target diff (so you can confirm nothing was dropped).
//   # Omit --apply for a SQL-only dry run; you can apply by hand later.
//
// IDEMPOTENCY
//   All emitted statements use `INSERT OR IGNORE` keyed on the primary key
//   (user_id / id), so re-running is safe — duplicate runs are no-ops.
//
// SCOPE
//   - users table: from `user:<userId>` blobs in USERS KV.
//     `email:`/`cust:` index keys in USERS KV are IGNORED — they're
//     redundant with the UNIQUE constraints in the new schema.
//   - runs  table: from `run:<userId>:<runId>` blobs in RUNS KV.
//     `runs:<userId>` index keys in RUNS KV are IGNORED — D1's
//     `idx_runs_user_created` replaces them.
//   - quota:<userId>:<YYYY-MM> counters STAY in USERS KV (out of scope —
//     we only migrated user records and run history per Task #25).

import { spawn } from "node:child_process";
import { writeFileSync, existsSync } from "node:fs";
import { fileURLToPath } from "node:url";
import { dirname, join, resolve as resolvePath } from "node:path";

const __dirname = dirname(fileURLToPath(import.meta.url));

// Resolve wrangler from the worker package's node_modules first (so this
// script works from any CWD without requiring a global install), then
// fall back to `npx wrangler`, then a bare `wrangler`. Each candidate
// is verified before use.
const WRANGLER = resolveWranglerCmd();

const args = parseArgs(process.argv.slice(2));
const ENV = args.env || "production";
const OUTPUT = resolvePath(args.output || "migrate-kv-to-d1.sql");
const VERBOSE = !!args.verbose;
const APPLY_DB = args.apply || null;  // e.g. "algosize" — if set, run d1 execute + diff
// Each KV source is identified either by a binding name (`--binding`-style,
// reads wrangler.toml) or a raw namespace id (works even when the binding
// has been removed). USERS still exists as a binding (quota counters);
// RUNS does not — Task #25 removed it — so the operator must supply
// --runs-namespace-id.
const USERS_SOURCE = args["users-namespace-id"]
  ? { kind: "id",      value: args["users-namespace-id"] }
  : { kind: "binding", value: "USERS" };
const RUNS_SOURCE = args["runs-namespace-id"]
  ? { kind: "id",      value: args["runs-namespace-id"] }
  : args["skip-runs"]
    ? null
    : { kind: "binding", value: "RUNS" };  // only resolves if op temporarily restored the binding

console.error(`[migrate] env=${ENV}  output=${OUTPUT}`);
console.error(`[migrate] users source: ${USERS_SOURCE.kind}=${USERS_SOURCE.value}`);
console.error(`[migrate] runs  source: ${RUNS_SOURCE ? `${RUNS_SOURCE.kind}=${RUNS_SOURCE.value}` : "SKIPPED (--skip-runs)"}`);

const sqlChunks = [
  "-- Generated by worker/scripts/migrate-kv-to-d1.mjs",
  `-- Source env: ${ENV}`,
  `-- Generated at: ${new Date().toISOString()}`,
  "-- Re-running this file is safe; INSERT OR IGNORE skips existing rows.",
  "",
  "BEGIN TRANSACTION;",
];

let userCount = 0;
let runCount  = 0;
let skipped   = 0;

// ---- users -----------------------------------------------------------
const userKeys = await listKeys(USERS_SOURCE, "user:");
console.error(`[migrate] found ${userKeys.length} user:* keys`);
for (const key of userKeys) {
  const raw = await getKey(USERS_SOURCE, key);
  if (!raw) { skipped++; continue; }
  let u;
  try { u = JSON.parse(raw); }
  catch { console.error(`[migrate] WARN bad JSON at ${key}`); skipped++; continue; }
  if (!u.userId || !u.email) { skipped++; continue; }

  // Old free-tier rows used "" or undefined for stripeCustomerId; the new
  // schema's UNIQUE constraint allows multiple NULLs but not multiple "".
  const stripeCustomerId = u.stripeCustomerId ? u.stripeCustomerId : null;
  const subStatus        = u.subStatus ?? null;
  const plan             = u.plan || (stripeCustomerId ? "paid" : "free");
  const createdAt        = u.createdAt ?? Math.floor(Date.now() / 1000);
  const updatedAt        = u.updatedAt ?? createdAt;

  sqlChunks.push(
    `INSERT OR IGNORE INTO users (user_id, email, stripe_customer_id, plan, sub_status, created_at, updated_at) VALUES (${
      [u.userId, u.email.toLowerCase(), stripeCustomerId, plan, subStatus, createdAt, updatedAt].map(sqlLit).join(", ")
    });`,
  );
  userCount++;
}

// ---- runs ------------------------------------------------------------
const runKeys = RUNS_SOURCE ? await listKeys(RUNS_SOURCE, "run:") : [];
console.error(`[migrate] found ${runKeys.length} run:* keys`);
for (const key of runKeys) {
  const raw = await getKey(RUNS_SOURCE, key);
  if (!raw) { skipped++; continue; }
  let r;
  try { r = JSON.parse(raw); }
  catch { console.error(`[migrate] WARN bad JSON at ${key}`); skipped++; continue; }
  if (!r.id || !r.userId || !r.analyzer) { skipped++; continue; }

  const inputJson  = r.input  === undefined ? null : JSON.stringify(r.input);
  const resultJson = r.result === undefined ? null : JSON.stringify(r.result);
  const ms         = typeof r.ms === "number" ? r.ms : null;
  const headline   = r.headline || "";
  const createdAt  = r.createdAt || Date.now();

  sqlChunks.push(
    `INSERT OR IGNORE INTO runs (id, user_id, analyzer, input_json, result_json, ms, headline, created_at) VALUES (${
      [r.id, r.userId, r.analyzer, inputJson, resultJson, ms, headline, createdAt].map(sqlLit).join(", ")
    });`,
  );
  runCount++;
}

sqlChunks.push("COMMIT;", "");
writeFileSync(OUTPUT, sqlChunks.join("\n"));

console.error(
  `[migrate] wrote ${OUTPUT}: ${userCount} users, ${runCount} runs, ${skipped} skipped`,
);

if (APPLY_DB) {
  console.error(`[migrate] applying ${OUTPUT} to D1 database '${APPLY_DB}' (env=${ENV}) …`);
  await runWrangler([
    "d1", "execute", APPLY_DB,
    "--env", ENV,
    "--remote",
    "--file", OUTPUT,
  ], { inheritStdio: true });

  const dbUsers = await countD1Rows(APPLY_DB, "users");
  const dbRuns  = await countD1Rows(APPLY_DB, "runs");

  // Diff report: source counts (this run) vs target counts (D1 right now,
  // post-apply). The target can legitimately be HIGHER than the source on
  // re-runs (rows from a previous migration are still there) but NEVER
  // lower for a fresh apply. Anything else is a red flag.
  const userDelta = dbUsers - userCount;
  const runDelta  = dbRuns  - runCount;
  console.error(
    "\n[migrate] ===== diff report =====\n" +
    `[migrate]   users  source=${userCount}  target=${dbUsers}  delta=${signed(userDelta)}\n` +
    `[migrate]   runs   source=${runCount}   target=${dbRuns}  delta=${signed(runDelta)}\n` +
    `[migrate]   skipped (bad JSON / missing keys): ${skipped}`,
  );
  const drift =
    (userDelta < 0 ? "users target < source — D1 dropped rows" : null) ||
    (runDelta  < 0 ? "runs target < source — D1 dropped rows"  : null);
  if (drift) {
    console.error(`[migrate] FAIL: ${drift}`);
    process.exit(2);
  }
  console.error("[migrate] OK — no missing rows in D1");
} else {
  console.error(
    `[migrate] apply with: ${WRANGLER.join(" ")} d1 execute <db> --env ${ENV} --file=${OUTPUT} --remote\n` +
    `[migrate] then re-run with --apply <db> to see the source-vs-target diff report`,
  );
}

// ---------------------------------------------------------------------
function parseArgs(argv) {
  const out = {};
  for (let i = 0; i < argv.length; i++) {
    const a = argv[i];
    if (a.startsWith("--")) {
      const [k, v] = a.slice(2).split("=");
      out[k] = v ?? argv[++i] ?? true;
    }
  }
  return out;
}

// SQLite literal: numbers as-is, NULL for null/undefined, single-quoted
// with embedded apostrophes doubled for strings.
function sqlLit(v) {
  if (v === null || v === undefined) return "NULL";
  if (typeof v === "number") return Number.isFinite(v) ? String(v) : "NULL";
  if (typeof v === "boolean") return v ? "1" : "0";
  return "'" + String(v).replace(/'/g, "''") + "'";
}

// Run `wrangler kv key list --env <ENV> --binding <namespace>` and parse
// the JSON it prints, paginating with --cursor. A single call returns at
// most ~1000 keys; without pagination we'd silently truncate large
// accounts and lose user/run data on migration.
async function listKeys(source, prefix) {
  const all = [];
  let cursor = null;
  for (let page = 0; page < 10000; page++) {
    const args = [
      "kv", "key", "list",
      "--env", ENV,
      ...sourceFlag(source),
      "--prefix", prefix,
    ];
    if (cursor) args.push("--cursor", cursor);
    const out = await runWrangler(args);
    let parsed;
    try { parsed = JSON.parse(out); }
    catch (e) {
      throw new Error(`failed to parse wrangler kv key list output: ${e.message}\n${out.slice(0, 400)}`);
    }
    // Wrangler 3.x prints either an array of {name, ...} (no pagination
    // info) or an object {keys, list_complete, cursor}. Handle both.
    let keys, listComplete, nextCursor;
    if (Array.isArray(parsed)) {
      keys = parsed;
      listComplete = true;
      nextCursor = null;
    } else {
      keys = parsed.keys || [];
      listComplete = parsed.list_complete !== false;
      nextCursor = parsed.cursor || null;
    }
    for (const k of keys) {
      if (k && k.name && k.name.startsWith(prefix)) all.push(k.name);
    }
    if (listComplete || !nextCursor) break;
    cursor = nextCursor;
    if (VERBOSE) console.error(`[migrate]   page ${page + 1}: ${all.length} ${prefix}* keys so far`);
  }
  return all;
}

async function getKey(source, key) {
  // `wrangler kv key get` prints the raw value to stdout (no JSON wrapping).
  // Returns "" for keys that exist but are empty; null for missing.
  try {
    return await runWrangler([
      "kv", "key", "get",
      "--env", ENV,
      ...sourceFlag(source),
      key,
    ]);
  } catch (e) {
    if (VERBOSE) console.error(`[migrate] miss ${source.kind}=${source.value}:${key}`);
    return null;
  }
}

function sourceFlag(source) {
  return source.kind === "id"
    ? ["--namespace-id", source.value]
    : ["--binding", source.value];
}

function runWrangler(extraArgs, { inheritStdio = false } = {}) {
  const [cmd, ...prefixArgs] = WRANGLER;
  const fullArgs = [...prefixArgs, ...extraArgs];
  return new Promise((resolve, reject) => {
    const child = spawn(cmd, fullArgs, {
      stdio: inheritStdio ? "inherit" : ["ignore", "pipe", "pipe"],
    });
    let stdout = "", stderr = "";
    if (!inheritStdio) {
      child.stdout.on("data", (b) => (stdout += b.toString()));
      child.stderr.on("data", (b) => (stderr += b.toString()));
    }
    child.on("error", reject);
    child.on("close", (code) => {
      if (code === 0) resolve(stdout);
      else reject(new Error(`${cmd} ${fullArgs.join(" ")} exited ${code}\n${stderr}`));
    });
  });
}

// Resolve a runnable wrangler invocation: prefer the local install in
// worker/node_modules/.bin so the script works without a global wrangler.
function resolveWranglerCmd() {
  // Walk up looking for node_modules/.bin/wrangler.
  let dir = __dirname;
  for (let i = 0; i < 6; i++) {
    const candidate = join(dir, "node_modules", ".bin", "wrangler");
    if (existsSync(candidate)) return [candidate];
    const parent = dirname(dir);
    if (parent === dir) break;
    dir = parent;
  }
  // Fall back to `npx --no-install wrangler` (uses any wrangler reachable
  // from the project's package tree without re-fetching).
  return ["npx", "--no-install", "wrangler"];
}

// Run a `SELECT COUNT(*)` against the D1 database and parse the JSON
// wrangler emits with --json.
async function countD1Rows(db, table) {
  const out = await runWrangler([
    "d1", "execute", db,
    "--env", ENV,
    "--remote",
    "--json",
    "--command", `SELECT COUNT(*) AS n FROM ${table};`,
  ]);
  // wrangler 3.x prints either an array of result groups or { results: [...] }.
  let parsed;
  try { parsed = JSON.parse(out); }
  catch { return -1; }
  const groups = Array.isArray(parsed) ? parsed : [parsed];
  for (const g of groups) {
    const rows = g.results || g.result || [];
    if (rows.length && typeof rows[0].n === "number") return rows[0].n;
  }
  return -1;
}

function signed(n) {
  return n > 0 ? `+${n}` : `${n}`;
}
