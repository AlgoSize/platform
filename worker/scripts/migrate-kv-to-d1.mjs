#!/usr/bin/env node
// One-shot migration tool: KV → D1 (Task #25).
//
// Reads the existing user records and run history out of Cloudflare KV
// (USERS namespace + RUNS namespace) and emits SQL that, when applied via
// `wrangler d1 execute`, populates the new `users` and `runs` tables in
// the D1 database.
//
// USAGE
//   # 1. Make sure wrangler is authenticated and you can list both
//   #    namespaces locally first:
//   wrangler kv key list --env production --binding USERS  | head
//   wrangler kv key list --env production --binding RUNS   | head
//
//   # 2. Generate the SQL bundle (writes to ./migrate-kv-to-d1.sql by default).
//   node worker/scripts/migrate-kv-to-d1.mjs --env production
//
//   # 3. Apply it to the D1 database (production OR a clone for testing).
//   wrangler d1 execute algosize --file=worker/migrate-kv-to-d1.sql --remote
//
// IDEMPOTENCY
//   All emitted statements use `INSERT OR IGNORE` keyed on the primary key
//   (user_id / id), so re-running is safe — duplicate runs are no-ops.
//
// SCOPE
//   - users table: from `user:<userId>` blobs in USERS KV.
//     `email:`/`cust:` index keys in USERS KV are IGNORED — they're
//     redundant with the UNIQUE constraints in the new schema.
//   - runs  table: from `run:<userId>:<runId>` blobs in RUNS KV.
//     `runs:<userId>` index keys in RUNS KV are IGNORED — D1's
//     `idx_runs_user_created` replaces them.
//   - quota:<userId>:<YYYY-MM> counters STAY in USERS KV (out of scope —
//     we only migrated user records and run history per Task #25).

import { spawn } from "node:child_process";
import { writeFileSync } from "node:fs";

const args = parseArgs(process.argv.slice(2));
const ENV = args.env || "production";
const OUTPUT = args.output || "migrate-kv-to-d1.sql";
const VERBOSE = !!args.verbose;

console.error(`[migrate] env=${ENV}  output=${OUTPUT}`);

const sqlChunks = [
  "-- Generated by worker/scripts/migrate-kv-to-d1.mjs",
  `-- Source env: ${ENV}`,
  `-- Generated at: ${new Date().toISOString()}`,
  "-- Re-running this file is safe; INSERT OR IGNORE skips existing rows.",
  "",
  "BEGIN TRANSACTION;",
];

let userCount = 0;
let runCount  = 0;
let skipped   = 0;

// ---- users -----------------------------------------------------------
const userKeys = await listKeys("USERS", "user:");
console.error(`[migrate] found ${userKeys.length} user:* keys`);
for (const key of userKeys) {
  const raw = await getKey("USERS", key);
  if (!raw) { skipped++; continue; }
  let u;
  try { u = JSON.parse(raw); }
  catch { console.error(`[migrate] WARN bad JSON at ${key}`); skipped++; continue; }
  if (!u.userId || !u.email) { skipped++; continue; }

  // Old free-tier rows used "" or undefined for stripeCustomerId; the new
  // schema's UNIQUE constraint allows multiple NULLs but not multiple "".
  const stripeCustomerId = u.stripeCustomerId ? u.stripeCustomerId : null;
  const subStatus        = u.subStatus ?? null;
  const plan             = u.plan || (stripeCustomerId ? "paid" : "free");
  const createdAt        = u.createdAt ?? Math.floor(Date.now() / 1000);
  const updatedAt        = u.updatedAt ?? createdAt;

  sqlChunks.push(
    `INSERT OR IGNORE INTO users (user_id, email, stripe_customer_id, plan, sub_status, created_at, updated_at) VALUES (${
      [u.userId, u.email.toLowerCase(), stripeCustomerId, plan, subStatus, createdAt, updatedAt].map(sqlLit).join(", ")
    });`,
  );
  userCount++;
}

// ---- runs ------------------------------------------------------------
const runKeys = await listKeys("RUNS", "run:");
console.error(`[migrate] found ${runKeys.length} run:* keys`);
for (const key of runKeys) {
  const raw = await getKey("RUNS", key);
  if (!raw) { skipped++; continue; }
  let r;
  try { r = JSON.parse(raw); }
  catch { console.error(`[migrate] WARN bad JSON at ${key}`); skipped++; continue; }
  if (!r.id || !r.userId || !r.analyzer) { skipped++; continue; }

  const inputJson  = r.input  === undefined ? null : JSON.stringify(r.input);
  const resultJson = r.result === undefined ? null : JSON.stringify(r.result);
  const ms         = typeof r.ms === "number" ? r.ms : null;
  const headline   = r.headline || "";
  const createdAt  = r.createdAt || Date.now();

  sqlChunks.push(
    `INSERT OR IGNORE INTO runs (id, user_id, analyzer, input_json, result_json, ms, headline, created_at) VALUES (${
      [r.id, r.userId, r.analyzer, inputJson, resultJson, ms, headline, createdAt].map(sqlLit).join(", ")
    });`,
  );
  runCount++;
}

sqlChunks.push("COMMIT;", "");
writeFileSync(OUTPUT, sqlChunks.join("\n"));

console.error(
  `[migrate] wrote ${OUTPUT}: ${userCount} users, ${runCount} runs, ${skipped} skipped\n` +
  `[migrate] apply with: wrangler d1 execute algosize --file=${OUTPUT} --remote\n` +
  `[migrate] after apply, verify: wrangler d1 execute algosize --remote ` +
  `--command="SELECT COUNT(*) FROM users; SELECT COUNT(*) FROM runs;" — counts ` +
  `should match the lines above.`,
);

// ---------------------------------------------------------------------
function parseArgs(argv) {
  const out = {};
  for (let i = 0; i < argv.length; i++) {
    const a = argv[i];
    if (a.startsWith("--")) {
      const [k, v] = a.slice(2).split("=");
      out[k] = v ?? argv[++i] ?? true;
    }
  }
  return out;
}

// SQLite literal: numbers as-is, NULL for null/undefined, single-quoted
// with embedded apostrophes doubled for strings.
function sqlLit(v) {
  if (v === null || v === undefined) return "NULL";
  if (typeof v === "number") return Number.isFinite(v) ? String(v) : "NULL";
  if (typeof v === "boolean") return v ? "1" : "0";
  return "'" + String(v).replace(/'/g, "''") + "'";
}

// Run `wrangler kv key list --env <ENV> --binding <namespace>` and parse
// the JSON it prints, paginating with --cursor. A single call returns at
// most ~1000 keys; without pagination we'd silently truncate large
// accounts and lose user/run data on migration.
async function listKeys(binding, prefix) {
  const all = [];
  let cursor = null;
  for (let page = 0; page < 10000; page++) {
    const args = [
      "kv", "key", "list",
      "--env", ENV,
      "--binding", binding,
      "--prefix", prefix,
    ];
    if (cursor) args.push("--cursor", cursor);
    const out = await runWrangler(args);
    let parsed;
    try { parsed = JSON.parse(out); }
    catch (e) {
      throw new Error(`failed to parse wrangler kv key list output: ${e.message}\n${out.slice(0, 400)}`);
    }
    // Wrangler 3.x prints either an array of {name, ...} (no pagination
    // info) or an object {keys, list_complete, cursor}. Handle both.
    let keys, listComplete, nextCursor;
    if (Array.isArray(parsed)) {
      keys = parsed;
      listComplete = true;
      nextCursor = null;
    } else {
      keys = parsed.keys || [];
      listComplete = parsed.list_complete !== false;
      nextCursor = parsed.cursor || null;
    }
    for (const k of keys) {
      if (k && k.name && k.name.startsWith(prefix)) all.push(k.name);
    }
    if (listComplete || !nextCursor) break;
    cursor = nextCursor;
    if (VERBOSE) console.error(`[migrate]   page ${page + 1}: ${all.length} ${prefix}* keys so far`);
  }
  return all;
}

async function getKey(binding, key) {
  // `wrangler kv key get` prints the raw value to stdout (no JSON wrapping).
  // Returns "" for keys that exist but are empty; null for missing.
  try {
    return await runWrangler([
      "kv", "key", "get",
      "--env", ENV,
      "--binding", binding,
      key,
    ]);
  } catch (e) {
    if (VERBOSE) console.error(`[migrate] miss ${binding}:${key}`);
    return null;
  }
}

function runWrangler(args) {
  return new Promise((resolve, reject) => {
    const child = spawn("wrangler", args, { stdio: ["ignore", "pipe", "pipe"] });
    let stdout = "", stderr = "";
    child.stdout.on("data", (b) => (stdout += b.toString()));
    child.stderr.on("data", (b) => (stderr += b.toString()));
    child.on("error", reject);
    child.on("close", (code) => {
      if (code === 0) resolve(stdout);
      else reject(new Error(`wrangler ${args.join(" ")} exited ${code}\n${stderr}`));
    });
  });
}
